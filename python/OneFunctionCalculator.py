firstNumber = int(input("Enter Your First Number: "))
secondNumber = int(input("Enter Your Second Number: "))

#subtraction
container = firstNumber - secondNumber
print(str(firstNumber) + " - " + str(secondNumber) + " = " + str(container))

#addition
container1 = firstNumber + secondNumber
print(str(firstNumber) + " + " + str(secondNumber) + " = " + str(container1))

#multiplication
container2 = firstNumber * secondNumber
print(str(firstNumber) + " * " + str(secondNumber) + " = " + str(container2))

#division
container3 = firstNumber / secondNumber
print(str(firstNumber) + " / " + str(secondNumber) + " = " + str(container3))

#floor division
container4 = firstNumber // secondNumber
print(str(firstNumber) + " // " + str(secondNumber) + " = " + str(container4))

#modulus
container5 = firstNumber % secondNumber
print(str(firstNumber) + " % " + str(secondNumber) + " = " + str(container5))

#exponent
container6 = firstNumber ** secondNumber
print(str(firstNumber) + " ** " + str(secondNumber) + " = " + str(container6))